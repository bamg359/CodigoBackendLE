package apuntes;

public class While {


    public static void main(String[] args) {

        //Acumulador
        int num = 0;

        while(num <= 100){

            //System.out.println(num);

            num++;
        }

        //contador

        int sum = 0;
        int numero = 0;

        while(numero <= 10){

            sum = sum + numero;
            System.out.println(sum);
            numero++;
        }






    }



}
